void LCD_According_16(uint16_t x,uint16_t y,uint8_t* data,uint16_t color)
{
	uint8_t num;
	int i,j,a;
	a = x;
	for(i=0;i<16;i++)
	{
		x = a;
		for(j=0;j<8;j++)
		{
			num = data[2*i]<<j;
			if(num & 0x80)
			{
				ILI9341_Draw_Point(x,y,color);
			}
			x++;
		}
		for(j=0;j<8;j++)
		{
			num = data[2*i+1]<<j;
			if(num & 0x80)
			{
				ILI9341_Draw_Point(x,y,color);
			}
			x++;
		}
		y++ ;
	}
}

